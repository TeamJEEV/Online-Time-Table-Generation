package Bean;

/**
 *
 * @author Harvey
 */
public class Department {

    private int id;
    private String name;
    private int HOD;
    private int faculty;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHOD() {
        return HOD;
    }

    public void setHOD(int HOD) {
        this.HOD = HOD;
    }

    public int getFaculty() {
        return faculty;
    }

    public void setFaculty(int faculty) {
        this.faculty = faculty;
    }
    
    
}
